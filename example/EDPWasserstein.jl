# ╔═╡ 753a6bc3-bdaf-4807-bef0-18171e4f2910
function otplan(μ, ν)
    # Use T instead of γ to indicate that this is a Monge map.
    T(x) = quantile(ν, cdf(μ, x))
    return T
end

